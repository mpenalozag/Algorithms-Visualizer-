def print_maze(maze, cols):
    """
    Esta funci칩n se encarga de printear los laberintos con un formato entendible.
    """
    for i in range(cols+1):
        print("--", end="")
    print()
    for row in maze:
        row_add = "|"
        for i in range(cols):
            row_add += row[i]
        row_add += "|"
        print(row_add)
    for i in range(cols+1):
        print("--", end="")
    print()



